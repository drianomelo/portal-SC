                <div>
                    <x-title img="" h2="Objetivos"
                        p="Objetivos do Conselho Municipal de Gestão Territorial"></x-title>
                    <ul class="flex flex-col gap-2 px-12 mt-2 list-disc dark:text-white ">
                        <li class="text-sm">Manifestar-se, de forma independente, sobre os processos de controle e
                            revisão
                            do Plano Diretor e dos demais instrumentos legais reguladores do desenvolvimento do
                            município;
                        </li>
                        <li class="text-sm">Pronunciar-se sobre as propostas, iniciativas, programas,
                            planos e projetos referentes ao desenvolvimento municipal, notadamente as que exerçam
                            impacto
                            sobre o espaço urbano e
                            sobre o território municipal;</li>
                        <li class="text-sm">Propor a edição de normas municipais de natureza urbanística e de gestão do
                            uso
                            e ocupação do território municipal e
                            manifestar-se sobre propostas de criação e alteração da legislação pertinente ao
                            desenvolvimento
                            municipal;</li>
                        <li class="text-sm">Propor medidas para a integração da política urbana e
                            territorial com as políticas socioeconômicas e ambientais municipais e regionais;
                        </li>
                        <li class="text-sm">Propor políticas para solução de conflitos na delimitação
                            dos limites do município;</li>
                        <li class="text-sm">Pronunciar-se sobre as ameaças à defesa do patrimônio
                            histórico, arquitetônico, natural e cultural do município;</li>
                        <li class="text-sm">Propor e apoiar a utilização de mecanismos de coopera-
                            ção com os governos federal e estadual, com municípios da
                            região metropolitana de Aracaju e com a sociedade local para
                            formulação e execução das políticas municipal e regional de
                            desenvolvimento;</li>
                        <li class="text-sm">Deliberar sobre a aplicação dos recursos financeiros do
                            Fundo Municipal de Desenvolvimento Territorial, bem como fiscalizar sua utilização;
                        </li>
                        <li class="text-sm">Acompanhar os estudos e relatórios de impacto ambiental e de impacto de
                            vizinhança sobre projetos, públicos ou privados, que venham causar impacto sobre o
                            território
                            municipal;
                        </li>
                        <li class="text-sm">Divulgar amplamente os dados e informações relativos à
                            sua atuação e aos temas que lhe são correlatos.
                        </li>
                    </ul>
                </div>

