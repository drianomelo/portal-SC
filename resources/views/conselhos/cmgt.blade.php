                        Fica criado o Conselho Municipal Gestão Territorial
                        (CMGT), órgão colegiado de caráter consultivo e deliberativo em
                        matéria de natureza urbanística e de política territorial, efetivo e
                        permanente tendo por finalidade garantir a participação dos diferentes segmentos da população na
                        formulação, implementação e
                        gestão da política territorial. Esse conselho substitui o Conselho
                        Municipal de Desenvolvimento, criado pela Lei Municipal 044/2019.
